package com.lambtoncollege.buildmypc.model;

public class Accessories {
    private String accID;
    private String accName;
    private String accBrand;
    private String accColour;
    private String accShortDesc;
    private String accLongDesc;
    private int accPrice;

    public String getAccID() {
        return accID;
    }

    public void setAccID(String accID) {
        this.accID = accID;
    }

    public String getAccName() {
        return accName;
    }

    public void setAccName(String accName) {
        this.accName = accName;
    }

    public String getAccBrand() {
        return accBrand;
    }

    public void setAccBrand(String accBrand) {
        this.accBrand = accBrand;
    }

    public String getAccColour() {
        return accColour;
    }

    public void setAccColour(String accColour) {
        this.accColour = accColour;
    }

    public String getAccShortDesc() {
        return accShortDesc;
    }

    public void setAccShortDesc(String accShortDesc) {
        this.accShortDesc = accShortDesc;
    }

    public String getAccLongDesc() {
        return accLongDesc;
    }

    public void setAccLongDesc(String accLongDesc) {
        this.accLongDesc = accLongDesc;
    }

    public int getAccPrice() {
        return accPrice;
    }

    public void setAccPrice(int accPrice) {
        this.accPrice = accPrice;
    }
}
