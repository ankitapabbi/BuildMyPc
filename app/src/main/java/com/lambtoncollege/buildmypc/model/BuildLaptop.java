package com.lambtoncollege.buildmypc.model;

public class BuildLaptop {
    private String buildLaptopID;
    private String screenSize;
    private String display;
    private String screenType;
    private String processor;
    private String processorGen;
    private String ram;
    private String storageType;
    private String graphicCard;
    private int price;

    public String getBuildLaptopID() {
        return buildLaptopID;
    }

    public void setBuildLaptopID(String buildLaptopID) {
        this.buildLaptopID = buildLaptopID;
    }

    public String getScreenSize() {
        return screenSize;
    }

    public void setScreenSize(String screenSize) {
        this.screenSize = screenSize;
    }

    public String getDisplay() {
        return display;
    }

    public void setDisplay(String display) {
        this.display = display;
    }

    public String getScreenType() {
        return screenType;
    }

    public void setScreenType(String screenType) {
        this.screenType = screenType;
    }

    public String getProcessor() {
        return processor;
    }

    public void setProcessor(String processor) {
        this.processor = processor;
    }

    public String getProcessorGen() {
        return processorGen;
    }

    public void setProcessorGen(String processorGen) {
        this.processorGen = processorGen;
    }

    public String getRam() {
        return ram;
    }

    public void setRam(String ram) {
        this.ram = ram;
    }

    public String getStorageType() {
        return storageType;
    }

    public void setStorageType(String storageType) {
        this.storageType = storageType;
    }

    public String getGraphicCard() {
        return graphicCard;
    }

    public void setGraphicCard(String graphicCard) {
        this.graphicCard = graphicCard;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
